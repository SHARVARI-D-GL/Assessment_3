package com.example.demo.controller;

public class Customer {
	private int custid;
	private String custname;
	private String custphone;
	private String custaddress;
	private String custloginid;
	private String custrpassword;
	public int getCustid() {
		return custid;
	}
	public void setCustid(int custid) {
		this.custid = custid;
	}
	public String getCustname() {
		return custname;
	}
	public void setCustname(String custname) {
		this.custname = custname;
	}
	public String getCustphone() {
		return custphone;
	}
	public void setCustphone(String custphone) {
		this.custphone = custphone;
	}
	public String getCustaddress() {
		return custaddress;
	}
	public void setCustaddress(String custaddress) {
		this.custaddress = custaddress;
	}
	public String getCustloginid() {
		return custloginid;
	}
	public void setCustloginid(String custloginid) {
		this.custloginid = custloginid;
	}
	public String getCustrpassword() {
		return custrpassword;
	}
	public void setCustrpassword(String custrpassword) {
		this.custrpassword = custrpassword;
	}
	

}